package com.ktdsuniversity.edu.stream.bigdata;

public class TextVO {
	
	private String key;
	private String value;
	
	
	String getKey() {
		return key;
	}
	void setKey(String key) {
		this.key = key;
	}
	String getValue() {
		return value;
	}
	void setValue(String value) {
		this.value = value;
	}
}
